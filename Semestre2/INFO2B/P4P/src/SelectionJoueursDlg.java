    private void BAjoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAjoutActionPerformed
        // TODO add your handling code here:
        int ind = this.LJoueurDispo.getSelectedIndex();
        if(ind != -1)
        {
            if(LJoueurs.getModel().getSize()<2)
            {
                //String ps = LJoueurDispo.getSelectedValue();
                DefaultListModel mod1 = (DefaultListModel) LJoueurDispo.getModel();
                DefaultListModel mod2 = (DefaultListModel) LJoueurs.getModel();
                mod2.addElement(LJoueurDispo.getSelectedValue());
                mod1.remove(ind);
                
    
            }
        }
    }//GEN-LAST:event_BAjoutActionPerformed

    private void BRetraitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BRetraitActionPerformed
        // TODO add your handling code here:
        int ind = this.LJoueurs.getSelectedIndex();
        if(ind != -1)
        {
            if(LJoueurs.getModel().getSize()>0){
           String ps = LJoueurs.getSelectedValue();
           DefaultListModel mod1 = (DefaultListModel) this.LJoueurDispo.getModel();
                DefaultListModel mod2 = (DefaultListModel) this.LJoueurs.getModel();
                mod1.addElement(ps);
                mod2.remove(ind);
            }

        }
    }//GEN-LAST:event_BRetraitActionPerformed

    private void BValiderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BValiderActionPerformed
        // TODO add your handling code here:
        DefaultListModel mod = (DefaultListModel) LJoueurs.getModel();
        if(mod.getSize() == 2){
           String ps1 = LJoueurs.getModel().getElementAt(0);
           String ps2 = LJoueurs.getModel().getElementAt(1); 
           this.joueur1 = this.lj.rechJoueur(ps1);
           this.joueur2 = this.lj.rechJoueur(ps2);
           this.ok = true;
            this.setVisible(false);
            this.dispose();
            
        }else 
            LMessage.setText("Il faut 2 joueurs");
        
    }//GEN-LAST:event_BValiderActionPerformed

