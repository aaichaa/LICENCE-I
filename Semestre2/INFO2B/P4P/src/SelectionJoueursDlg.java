    public void initListesJoueurs(){
        DefaultListModel mod = new DefaultListModel();
        LJoueurDispo.setModel(mod);
        for(int i = 0; i <this.lj.getNbJoueurs();i++){
            if(!this.lj.getJoueur(i).getPseudo().equals(joueur1.getPseudo())&&!this.lj.getJoueur(i).getPseudo().equals(joueur2.getPseudo()))
            
            mod.addElement(this.lj.getJoueur(i).getPseudo());
        }
         DefaultListModel mod1 = new DefaultListModel();
        LJoueurs.setModel(mod1);
          mod1.addElement(joueur1.getPseudo());
          mod1.addElement(joueur2.getPseudo());
        
    }
    
    
