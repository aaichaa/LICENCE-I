    public void melanger(){
        int r= (couleur1.getRed() + couleur2.getRed())/2 ;
        int v =(couleur1.getGreen() + couleur2.getGreen())/2;
        int b =(couleur1.getBlue() + couleur2.getBlue())/2;
        Color melange = new Color(r, v, b);
        PCentre.setBackground(melange);

    }
    
    
