    public void verifPersos(){
    //Déclarations
    Joueur jc = this.jeu.getJoueurCourant(); //Joueur Courant
    int c =  this.plateau.getNbcol();
    JButton b1 = (JButton) Panneau.getComponent(this.l1*c+this.c1);
    JButton b2 = (JButton) Panneau.getComponent(this.l2*c+this.c2);
    //Comparaison des deux cartes
    if(this.plateau.getCase(l1,c1)==this.plateau.getCase(l2, c2))
    {
        //Récuperation famille
        int n1 = this.plateau.getCase(l1, c1);
        String fam = this.persos.getPerso(n1).getFamille();
        //Appel traiterTour
        int bonus = this.jeu.TraiterTout(jc,n1);
        //traitement du bonus
        if(bonus >=0)
        {
            Edition.append("\n\n Bravo!\n"+jc.getPseudo()+" a obtenu tous les personnages de la famille : "+fam+"\n");
            TraitementBonus(bonus);
        }
        //Invalider les 2 cartes
        this.plateau.invalide(l1, c1, l2, c2);
        b1.setEnabled(false);
        b2.setEnabled(false);
        //Test fin du jeu
        if(this.plateau.jeuVide())
            resultatJeuMemory();
        
        else
            Edition.append("\n Tour avec "+jc.getPseudo()+"\n");
            NbPersosT.setText("Nombres de de personnages trouvé : "+(this.plateau.getNbc()-this.plateau.getNbp()));
            NbPersoR.setText("Nombres de de personnages restant : "+this.plateau.getNbp());
    }//Fin cartes identiques
    else
    {
        //Effacer cartes
        b1.setIcon(new ImageIcon(getClass().getResource("/Img/Dos.PNG")));
        b2.setIcon(new ImageIcon(getClass().getResource("/Img/Dos.PNG")));
        //Changer de joueur
        this.jeu.setJoueurCourant(this.jeu.getIndSuivant(this.jeu.getIndC()));
        //Mise à jour de JLabel JCLabel
        NbPersosT.setText("Nombres de de personnages trouvé :"+(this.plateau.getNbc()-this.plateau.getNbp()));
        NbPersoR.setText("Nombres de de personnages restant :"+this.plateau.getNbp());
        JC.setText("C'est à "+this.jeu.getJoueurCourant().getPseudo()+" de joueur !");
    }
    
    //Réinitialise les coordonnées l1, c1, l2 et c2 ç -1
    l1 = -1;c1 = -1;l2 = -1;c2 = -1;
    
    
}
    private void resultatJeuMemory()
    {
        Edition.append(" \n Fin du jeu "+this.joueurs);
        LesJoueurs gagants = this.joueurs.getGagnants();
        if (gagants.getNbJoueurs()==1) {
            Edition.append("Le gagant est "+gagants.getJoueur(0).getPseudo());}
         else{// plusieur gagants 
            Edition.append("Les gagants sont : \n ");
            for (int i = 0; i < gagants.getNbJoueurs(); i++) {
                Edition.append(gagants.getJoueur(i).getPseudo()+" ");
                Edition.append("\n");
            }
        }
        
    } 

  
    
    private void TraitementBonus(int b)
    {
        if (b==0) { //famille préferer
            Edition.append("c'est ma famille pref \n ");
            Edition.append(this.jeu.getJoueurCourant().getPseudo()+" a gagner");
            this.jeu.getPlateau().termineJeu();
            this.plateau = this.jeu.getPlateau();
            
            
            int nb = this.plateau.getNbc();
            for (int i = 0; i < nb*2; i++) {
                    JButton bt = (JButton) Panneau.getComponent(i);
                    bt.setEnabled(false);
            }
           
        }
        if (b == 1) {
            TransfertDlg diag = new TransfertDlg(this,true,this.joueurs,this.jeu.getIndC());
            diag.setVisible(true);
            if (diag.isOk()) {
                Edition.append(diag.getTc().toString()); // voir la diag de tr et qui récupère une instance de transfert 
                
                
            }
            Edition.append(" --> Mise a jour suite au transfert \n"+this.jeu.getJoueurCourant()+"\n");
        }
        if (b == 2) {
            BatailleDlg diag = new BatailleDlg(this,true,this.joueurs,this.jeu.getIndC());
            diag.setVisible(true);
            if (diag.isOk()) {
                Edition.append(diag.getBataille().toString());
            }else
                Edition.append(diag.getBataille().getDeroulement()+"\n");
                Edition.append("--> mise a jour suite à la bataille \n"+
                        this.jeu.getJoueurCourant()+"\n"+diag.getBataille().getAdversaire()+"\n");
        }
    
    }
