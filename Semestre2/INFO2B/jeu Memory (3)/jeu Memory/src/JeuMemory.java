    private void boutonActionPerformed(ActionEvent evt) {
            // récupérer le bouton 
            JButton bt = (JButton) evt.getSource();
            // récuperer son numéro
            int i = Integer.parseInt(bt.getName());
            
            
            // calsuler  ligne et colones correspondante 
            int lig = i/this.plateau.getNbcol();
            int col = i-lig*this.plateau.getNbcol();
            // récuperer la valeur et le personnage 
            int num = this.plateau.getCase(lig, col);
            Personnage p = this.persos.getPerso(num);
            // afficher phoo sur le bt
            p.setImgBouton(bt);
           
            // tester quelle case (1er ou 2eme)
            if (this.l1== -1 && this.c1== -1) {
                this.l1 = lig;
                this.c1=col;   
            }else if (this.l2==-1 && this.c2==-1) {
            this.l2=lig;
            this.c2=col;
            startTimer();
        }
            
     } 
